// CLASE ENEMIGO
public class Enemigo {
    private String nombre;
    private int salud;
    private int nivelDificultad;

    public Enemigo(String nombre, int salud, int nivelDificultad) {
        this.nombre = nombre;
        this.salud = salud;
        this.nivelDificultad = nivelDificultad;
    }
}
