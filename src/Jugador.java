// CLASE JUGADOR
public class Jugador {
    private String nombre;
    private int salud;
    private int nivel;
    private String habilidadEspecial;

    public Jugador(String nombre, int salud, int nivel, String habilidadEspecial) {
        this.nombre = nombre;
        this.salud = salud;
        this.nivel = nivel;
        this.habilidadEspecial = habilidadEspecial;
    }
}
