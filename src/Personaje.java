// CLASE Personaje
public class Personaje {
    private String nombre;
    private int salud;
    private int nivel;

    private int energia;


    public Personaje(String nombre, int salud, int nivel, int energia) {
        this.nombre = nombre;
        this.salud = salud;
        this.nivel = nivel;
        this.energia = energia;
    }

}
